public class diamondshape32 {
    public static void main(String[] args) {
        int rows = 5;
        for( int i=1;i<rows;i++ )
        { for (int j=1; j<rows-i;j++){
            System.out.println("");

        }
        for (int k=1; k<rows-i;k++){
            System.out.println("*");
        }
        System.out.println();
        
        }
    }
}
